
public class sucursal {
    Integer Id;
    String Nombre;
    
    public sucursal(){}

    public sucursal(Integer Id, String Nombre) {
        this.Id = Id;
        this.Nombre = Nombre;
    }

    public Integer getId() {
        return Id;
    }

    public void setId(Integer Id) {
        this.Id = Id;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }
    
    
    
    
}
